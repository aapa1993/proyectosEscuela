    private void jLabel12MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel12MouseClicked
        try {
            JFileChooser archivo = new JFileChooser();
            String text = jTextArea3.getText();
            int select = archivo.showOpenDialog(this);
            if (select == JFileChooser.APPROVE_OPTION) {
                BufferedWriter bw = new BufferedWriter(new FileWriter(archivo.getSelectedFile()));
                bw.write(text);
                bw.close();
                JOptionPane.showMessageDialog(this,"Programa creado por Abraham Augusto Pech Ayil");
            }
            
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jLabel12MouseClicked

