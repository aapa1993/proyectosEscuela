    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
        try {
            JFileChooser archivo = new JFileChooser();
            int resultado = archivo.showOpenDialog(this);
            if (resultado == JFileChooser.APPROVE_OPTION) {
                BufferedReader br = new BufferedReader(new FileReader(archivo.getSelectedFile()));
                String text = "";
                String line = "";
                while ((line = br.readLine()) != null) {
                    text += line + "\n";
                }

                jTextArea1.setText(text);
                br.close();
            }
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jLabel9MouseClicked

    private void jLabel10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel10MouseClicked
        if (jTextArea1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No hay texto para modificar");
        }else{
            String texto = jTextArea1.getText();
            Encriptacion x = new Encriptacion(texto);
            jTextArea3.setText(x.texto2);
        }
    }//GEN-LAST:event_jLabel10MouseClicked

